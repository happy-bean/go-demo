//标识符
//标识符用来命名变量、类型等程序实体。一个标识符实际上就是一个或是多个字母(A~Z和a~z)数字(0~9)、下划线_组成的序列，但是第一个字符必须是字母或下划线而不能是数字。
//以下是有效的标识符：
//mahesh   kumar   abc   move_name   a_123
//myname50   _temp   j   a23b9   retVal
//以下是无效的标识符：
//1ab（以数字开头）
//case（Go 语言的关键字）
//a+b（运算符是不允许的）

